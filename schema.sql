/* tạo bảng nhân viên*/
CREATE TABLE NHANVIEN 
(
  MANV NUMBER GENERATED ALWAYS as IDENTITY(START with 1 INCREMENT by 1)
, HOTEN VARCHAR2(50) 
, DIACHI VARCHAR2(50) 
, DIENTHOAI VARCHAR2(50) 
, EMAIL VARCHAR2(50) 
, MAPHONG NUMBER
, CHINHANH NUMBER
, LUONG NUMBER
, CHUCVU NUMBER 
, CONSTRAINT NHANVIEN_PK PRIMARY KEY 
  (
    MANV
  )
  ENABLE 
);
/* tạo bảng phong ban*/
CREATE TABLE PHONGBAN 
(
