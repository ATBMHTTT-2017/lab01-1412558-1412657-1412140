, TRUONGPHONG NUMBER 
, NGAYNHANCHUC DATE 
, SONHANVIEN NUMBER 
, CHINHANH NUMBER 
, CONSTRAINT PHONGBAN_PK PRIMARY KEY 
  (
    MAPHONG 
  )
  ENABLE 
);

